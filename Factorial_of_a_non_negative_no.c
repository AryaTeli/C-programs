#include <stdio.h>
void main()
{
    int i=1,fact=1,n;
    printf("Enter the non negative number :");
    scanf("%d" ,&n);
    if(n>=0)
    {
        while(i<=n)
        {
            fact=fact*i;
            i++;
        }
        printf("Factorial of %d is %d " ,n,fact);
    }
    else
    {
        printf("Invalid input");
    }
}
